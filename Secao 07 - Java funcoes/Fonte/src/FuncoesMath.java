public class FuncoesMath {
    public static void main(String[] args) {

        // Máximo entre dois números
        System.out.println(Math.max(10, 100));

        // Mínimo entre dois números
        System.out.println(Math.min(10, 100));

        // Raíz quadrada
        System.out.println(Math.sqrt(15425f));

        // Logarítimo
        System.out.println(Math.log10(10));

        // Potência
        System.out.println(Math.pow(10, 2));

        // PI
        System.out.println(Math.PI);

        // e - Euler
        System.out.println(Math.E);

        // Arredonta para número mais próximo
        System.out.println(Math.round(15254.456749842));

        // Número aleatório entre 0 (incluso) e 1 (não incluso) - [0, 1[
        System.out.println(Math.random());
    }
}
