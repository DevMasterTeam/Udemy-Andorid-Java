// Este programa calcula a área de um retângulo
public class Ex1 {
    public static void main(String[] args) {

        // Declara a largura e altura do retângulo
        int largura = 10; // Largura em unidades
        int altura = 5; // Altura em unidades

        // Calcula a área
        int area = largura * altura;

        // Imprime o resultado
        System.out.println("A área do retângulo é: " + area);
    }
}